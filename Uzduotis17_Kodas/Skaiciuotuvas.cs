

namespace Uzduotis17_Kodas
{
    public class Skaiciuotuvas
    {
        static public int Sudetis(int pirmSkaicius, int antrSkaicius)
        {
            return pirmSkaicius + antrSkaicius;
        }
        static public int Atimtis(int pirmSkaicius, int antrSkaicius)
        {
            return pirmSkaicius - antrSkaicius;
        }
        static public int Daugyba(int pirmSkaicius, int antrSkaicius)
        {
            return pirmSkaicius * antrSkaicius;
        }
        static public double Dalyba(int pirmSkaicius, int antrSkaicius)
        {
            return (pirmSkaicius / antrSkaicius);
        }
        static public int Liekana(int pirmSkaicius, int antrSkaicius)
        {
            return (pirmSkaicius % antrSkaicius);
        }
    }
}
